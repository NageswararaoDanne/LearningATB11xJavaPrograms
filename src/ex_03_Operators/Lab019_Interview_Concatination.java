package ex_03_Operators;

public class Lab019_Interview_Concatination {
    public static void main(String[] args) {

        //Concatination Process
        String daughter_name = "Dhrithi";
        String son_name = "Vedhansh";

        int d_age = 7;
        int s_age = 5;

        System.out.println(daughter_name + son_name + d_age + s_age);
        System.out.println(d_age + s_age+ daughter_name + son_name);

        System.out.println( (8 * 4 / 5 + 3 ) * 2);

        // 8*4 = 32
        // 32/5 = 6
        // 6+3 = 9
        // 9*2 = 18

    }
}
